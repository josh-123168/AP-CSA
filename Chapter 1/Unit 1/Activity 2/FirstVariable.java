public class FirstVariable
{
    public static void main(String[] args)
    {
        int age = 12;
        double allowance = 5.25;
        boolean isOldEnough = true;

        System.out.print("The value of age is ");
        System.out.println(age);
        System.out.print("The value of allowance is ");
        System.out.println(allowance);
        System.out.print("The value of isOldEnough is ");
        System.out.println(isOldEnough);
    }
}
